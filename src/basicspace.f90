      SUBROUTINE REGAT(NRESPONDENTS,NDIMENSIONS,NS,NF,A,Y,V)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION A(NRESPONDENTS,NDIMENSIONS+2),Y(NRESPONDENTS),       &
